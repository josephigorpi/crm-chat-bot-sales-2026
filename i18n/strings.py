def get_string(key: str, language: str = "es", **kwargs) -> str:
    """
    Obtiene un string del diccionario i18n.
    
    Args:
        key: Clave del string (ej: 'login_title')
        language: Idioma ('es' o 'en'). Por defecto 'es'
        **kwargs: Variables para format() (ej: username='Juan')
    
    Returns:
        String traducido, con variables reemplazadas si se proporcionan
    
    Example:
        get_string('login_title', language='es')
        get_string('dashboard_welcome', language='en', username='John')
    """
    if language not in STRINGS:
        language = "es"
    
    string = STRINGS[language].get(key, f"[{key}]")
    
    # Reemplazar variables si se proporcionan
    if kwargs:
        try:
            string = string.format(**kwargs)
        except KeyError as e:
            # Si falta una variable, dejarla sin reemplazar
            pass
    
    return string
